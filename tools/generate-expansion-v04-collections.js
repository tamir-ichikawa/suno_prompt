function ensureDir(dirPath) {
  fs.mkdirSync(dirPath, { recursive: true });
}

function pad(number, width = 3) {
  return String(number).padStart(width, "0");
}

function slugify(value) {
  return String(value || "")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function titleCase(value) {
  return String(value || "")
    .replace(/[_-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim()
    .replace(/\b\w/g, (char) => char.toUpperCase());
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function pick(values, index) {
  return values[index % values.length];
}

function energyForBpm(bpm) {
  if (bpm >= 138) return ["high", 9];
  if (bpm >= 112) return ["medium-high", 8];
  if (bpm >= 84) return ["medium", 6];
  return ["low-medium", 4];
}

function makeTitle(config, categoryName, categoryIndex, variationIndex) {
  return [
    config.titleWord,
    titleCase(categoryName),
    pick(titleA, categoryIndex + variationIndex),
    pick(titleB, categoryIndex * 2 + variationIndex),
    pick(titleC, categoryIndex * 3 + variationIndex),
  ].join(" ");
}

function makePrompt(config, subcategory, categoryIndex, variationIndex) {
  const variation = variations[variationIndex];
  const bpm = pick(config.bpms, categoryIndex + variationIndex);
  const key = pick(keys, categoryIndex * 2 + variationIndex);
  const groove = pick(config.grooves, categoryIndex);
  const drums = pick(config.drums, categoryIndex + variationIndex);
  const bass = pick(config.bass, categoryIndex + variationIndex);
  const colors = pick(config.colors, categoryIndex + variationIndex);
  const vocal = pick(config.vocals, categoryIndex + variationIndex);
  const label = config.label;

  const prompt =
    `Create an original ${subcategory} track at ${bpm} BPM in ${key}, shaped around ${groove}. ` +
    `Build the rhythm with ${drums}; use ${bass}. Feature ${colors}; ${vocal}. ` +
    `Keep hooks, motifs, riffs, chants, phrases, and melodies newly composed, with no named performer, title, character, film, game, television, brand, or recording reference. ` +
    `${variation.structure} ${variation.detail} Shape it for ${variation.useCase}, clear ${label} identity, and no borrowed melody. ` +
    `Mix with separated kick, bass, drums, leads, vocals, percussion, ambience, and clean headroom.`;

  if (prompt.length <= 1000) return { prompt, bpm, key, vocal };

  const shorter =
    `Create an original ${subcategory} track at ${bpm} BPM in ${key}, shaped around ${groove}. ` +
    `Use ${drums}, ${bass}, and ${colors}; ${vocal}. Keep every hook, motif, riff, phrase, and melody newly composed, with no named performer, title, character, film, game, television, brand, or recording reference. ` +
    `${variation.structure} Build clear section contrast, authentic ${label} vocabulary, and a clean mix with separated low end, percussion, lead texture, vocals, ambience, and headroom.`;

  if (shorter.length > 1000) {
    throw new Error(`${config.id} ${subcategory} prompt exceeds 1000 chars after fallback: ${shorter.length}`);
  }

  return { prompt: shorter, bpm, key, vocal };
}

function makeRecord(config, categoryName, categoryIndex, variationIndex, globalIndex) {
  const subcategory = slugify(categoryName);
  const { prompt, bpm, key, vocal } = makePrompt(config, categoryName, categoryIndex, variationIndex);
  const [energy, energyScore] = energyForBpm(bpm);
  const batchNumber = Math.floor(globalIndex / 50) + 1;
  const tags = unique([...config.baseTags, subcategory, slugify(config.region), slugify(config.titleWord)]);

  return {
    id: `${config.id}-${pad(globalIndex + 1)}`,
    source_type: SOURCE_TYPE,
    batch_id: `${DATE}-${config.batchStem}-${pad(batchNumber)}`,
    collection: config.id,
    language_region: config.region,
    category: config.category,
    subcategory,
    title: makeTitle(config, categoryName, categoryIndex, variationIndex),
    prompt,
    exclude: unique([...commonExclude, ...config.excludeExtra]).join(", "),
    bpm,
    key,
    language: config.language || "English",
    vocal,
    mood: [config.label, "clear structure", "public safe prompt"],
    tags,
    groove_score: 7 + ((categoryIndex + variationIndex) % 4),
    energy,
    energy_score: energyScore,
    public_safe: true,
    rights_status: "ai_generated_original",
    needs_review: false,
    is_top_pick: variationIndex === 0,
  };
}

function buildConfig(config) {
  if (config.categories.length !== 40) {
    throw new Error(`${config.id} must have exactly 40 categories, got ${config.categories.length}`);
  }

  const records = [];
  config.categories.forEach((categoryName, categoryIndex) => {
    variations.forEach((_, variationIndex) => {
      records.push(makeRecord(config, categoryName, categoryIndex, variationIndex, records.length));
    });
  });

  const titleSet = new Set(records.map((record) => record.title.toLowerCase()));
  if (titleSet.size !== records.length) {
    throw new Error(`${config.id} generated duplicate titles`);
  }

  return records;
}

function writeJsonl(filePath, records) {
  fs.writeFileSync(filePath, `${records.map((record) => JSON.stringify(record)).join("\n")}\n`, "utf8");
}

function main() {
  ensureDir(OUT_DIR);
  ensureDir(COLLECTION_DIR);

  configs.forEach((config) => {
    const records = buildConfig(config);
    const allFile = path.join(OUT_DIR, `${config.id}-200.jsonl`);
    const collectionFile = path.join(COLLECTION_DIR, `${config.id}.jsonl`);
    writeJsonl(allFile, records);
    writeJsonl(collectionFile, records);

    for (let batchIndex = 0; batchIndex < 4; batchIndex += 1) {
      const batch = records.slice(batchIndex * 50, batchIndex * 50 + 50);
      const batchFile = path.join(OUT_DIR, `${DATE}-${config.batchStem}-${pad(batchIndex + 1)}.jsonl`);
      writeJsonl(batchFile, batch);
    }

    const promptLengths = records.map((record) => record.prompt.length);
    console.log(
      JSON.stringify({
        collection: config.id,
        records: records.length,
        prompt_min: Math.min(...promptLengths),
        prompt_max: Math.max(...promptLengths),
        allFile,
        collectionFile,
      })
    );
  });
}

