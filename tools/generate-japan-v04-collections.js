function slugify(value) {
  return String(value || "")
    .normalize("NFKD")
    .toLowerCase()
    .replace(/&/g, " and ")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "");
}

function pad(value, width = 3) {
  return String(value).padStart(width, "0");
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function pick(values, a, b = 0) {
  return values[(a * 3 + b) % values.length];
}

function titleFor(config, categoryIndex, variationIndex) {
  return `${config.titleWord} ${titleA[(categoryIndex * 2 + variationIndex) % titleA.length]} ${
    titleB[(categoryIndex * 5 + variationIndex * 3) % titleB.length]
  } ${titleC[(categoryIndex * 7 + variationIndex * 2) % titleC.length]}`;
}

function makePrompt(config, categoryLabel, categoryIndex, variationIndex) {
  const variation = variations[variationIndex];
  const bpm = config.bpm[variationIndex];
  const key = keys[(categoryIndex + variationIndex) % keys.length];
  const groove = pick(config.grooves, categoryIndex, variationIndex);
  const drums = pick(config.drums, categoryIndex, variationIndex);
  const bass = pick(config.bass, categoryIndex, variationIndex);
  const colors = pick(config.colors, categoryIndex, variationIndex);
  const vocal = pick(config.vocals, categoryIndex, variationIndex);

  const categorySentence = `Use the ${categoryLabel} category as the guiding vocabulary, but describe it through arrangement, rhythm, instrumentation, BPM, key, texture, and structure rather than named references.`;
  const full = [
    `Create an original ${categoryLabel} track at ${bpm} BPM in ${key}, built around ${groove}.`,
    `Build the rhythm with ${drums}; use ${bass}.`,
    `Feature ${colors}; use ${vocal}.`,
    categorySentence,
    variation.structure,
    variation.detail,
    `Shape it for ${variation.useCase}, clear Japanese genre identity, memorable original motif, and no borrowed melody. Mix with separated kick, bass, guitars, keys, leads, vocals, percussion, ambience, and master dynamics.`,
  ].join(" ");
  if (full.length <= 1000) return { prompt: ensureLength(full, config, categoryLabel), bpm, key, vocal };

  const compact = [
    `Create an original ${categoryLabel} track at ${bpm} BPM in ${key}, built around ${groove}.`,
    `Build the rhythm with ${drums}; use ${bass}.`,
    `Feature ${colors}; use ${vocal}.`,
    `Keep hooks, motifs, riffs, phrases, and melodies newly composed, with no named performer, title, character, game, anime, agency, or recording references.`,
    variation.structure,
    `Shape it for ${variation.useCase}, clear Japanese genre identity, and no borrowed melody. Mix with separated kick, bass, guitars, keys, leads, vocals, percussion, and ambience.`,
  ].join(" ");
  if (compact.length <= 1000) return { prompt: ensureLength(compact, config, categoryLabel), bpm, key, vocal };

  const short = [
    `Create an original ${categoryLabel} track at ${bpm} BPM in ${key}, built around ${groove}.`,
    `Use ${drums}, ${bass}, and ${colors}.`,
    `${vocal}. Keep hooks, motifs, riffs, phrases, and melodies newly composed, with no named performer, title, character, game, anime, agency, or recording references.`,
    variation.structure,
    `Mix with separated kick, bass, guitars, keys, leads, vocals, percussion, and ambience.`,
  ].join(" ");
  return { prompt: ensureLength(short, config, categoryLabel), bpm, key, vocal };
}

function ensureLength(prompt, config, categoryLabel) {
  let value = prompt;
  const additions = [
    " Preserve human timing, section contrast, and a hook shape that Suno can follow without relying on any famous reference.",
    " Add tasteful automation, fills, and counterlines so later sections develop while the core identity stays clear.",
    ` Keep the ${categoryLabel} idea specific through tempo, groove, instruments, vocal type, arrangement, and exclude rules.`,
  ];
  for (const addition of additions) {
    if (value.length >= 650) break;
    if (value.length + addition.length <= 1000) value += addition;
  }
  if (value.length < 650) throw new Error(`${config.id} ${categoryLabel} prompt too short: ${value.length}`);
  if (value.length > 1000) throw new Error(`${config.id} ${categoryLabel} prompt too long: ${value.length}`);
  return value;
}

function makeExclude(config) {
  return unique([
    "specific artist references",
    "copyrighted melodies",
    "existing song titles",
    "specific anime titles",
    "specific game titles",
    "character names",
    "brand names",
    "copied hooks",
    "recognizable samples",
    "style imitation",
    "fake accent caricature",
    "muddy low end",
    "harsh clipping",
    "overcompressed master",
    ...config.excludeExtra,
  ]).join(", ");
}

function makeRecords(config) {
  if (config.categories.length !== 40) {
    throw new Error(`${config.id} expected 40 categories, got ${config.categories.length}`);
  }

  const records = [];
  config.categories.forEach((categoryLabel, categoryIndex) => {
    const subcategory = slugify(categoryLabel);
    variations.forEach((_, variationIndex) => {
      const index = records.length;
      const batchNumber = Math.floor(index / 50) + 1;
      const { prompt, bpm, key, vocal } = makePrompt(config, categoryLabel, categoryIndex, variationIndex);
      const tags = unique([...config.baseTags, subcategory, slugify(config.region)]);
      records.push({
        id: `${config.id}-${pad(index + 1)}`,
        source_type: SOURCE_TYPE,
        batch_id: `${DATE}-${config.batchStem}-${pad(batchNumber)}`,
        collection: config.id,
        language_region: config.region,
        category: config.category,
        subcategory,
        title: titleFor(config, categoryIndex, variationIndex),
        prompt,
        exclude: makeExclude(config),
        bpm,
        key,
        vocal,
        mood: ["Japanese genre vocabulary", "clear structure", "public safe prompt"],
        tags,
        groove_score: variationIndex === 0 ? 9 : 8,
        energy: bpm >= 130 ? "high" : bpm >= 96 ? "medium-high" : "medium",
        energy_score: bpm >= 130 ? 9 : bpm >= 96 ? 8 : 6,
        public_safe: true,
        rights_status: "ai_generated_original",
        needs_review: false,
        is_top_pick: variationIndex === 0 && categoryIndex % 5 === 0,
      });
    });
  });

  if (records.length !== 200) throw new Error(`${config.id} expected 200 records, got ${records.length}`);
  const titleSet = new Set();
  const idSet = new Set();
  records.forEach((record) => {
    const titleKey = record.title.toLowerCase();
    if (titleSet.has(titleKey)) throw new Error(`${config.id} duplicate title: ${record.title}`);
    titleSet.add(titleKey);
    if (idSet.has(record.id)) throw new Error(`${config.id} duplicate id: ${record.id}`);
    idSet.add(record.id);
    if (record.prompt.length < 650 || record.prompt.length > 1000) {
      throw new Error(`${record.id} prompt length out of range: ${record.prompt.length}`);
    }
    if (/\r|\n/.test(record.prompt)) throw new Error(`${record.id} prompt contains newline`);
  });
  return records;
}

function writeCollection(config, records) {
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.mkdirSync(COLLECTION_DIR, { recursive: true });

  const allFile = path.join(OUT_DIR, `${config.id}-200.jsonl`);
  fs.writeFileSync(allFile, `${records.map((record) => JSON.stringify(record)).join("\n")}\n`, "utf8");

  const merged = [];
  for (let batchNumber = 1; batchNumber <= 4; batchNumber += 1) {
    const batchId = `${DATE}-${config.batchStem}-${pad(batchNumber)}`;
    const batch = records.filter((record) => record.batch_id === batchId);
    if (batch.length !== 50) throw new Error(`${batchId} expected 50 records, got ${batch.length}`);
    const batchFile = path.join(OUT_DIR, `${batchId}.jsonl`);
    fs.writeFileSync(batchFile, `${batch.map((record) => JSON.stringify(record)).join("\n")}\n`, "utf8");
    merged.push(...batch);
  }

  const collectionFile = path.join(COLLECTION_DIR, `${config.id}.jsonl`);
  fs.writeFileSync(collectionFile, `${merged.map((record) => JSON.stringify(record)).join("\n")}\n`, "utf8");
  return { allFile, collectionFile, count: records.length };
}

const summary = {};
configs.forEach((config) => {
  const records = makeRecords(config);
  const output = writeCollection(config, records);
  summary[config.id] = { count: output.count, allFile: output.allFile, collectionFile: output.collectionFile };
});

