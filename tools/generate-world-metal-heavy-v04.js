function pad(value, width = 3) {
  return String(value).padStart(width, "0");
}

function unique(values) {
  return [...new Set(values.filter(Boolean))];
}

function titleFor(profileIndex, variationIndex) {
  const adjective = titleAdjectives[(profileIndex * 3 + variationIndex) % titleAdjectives.length];
  const noun = titleNouns[(profileIndex * 7 + variationIndex * 3) % titleNouns.length];
  const action = titleActions[(profileIndex * 5 + variationIndex * 2) % titleActions.length];
  return `${adjective} ${noun} ${action}`;
}

function makePrompt(profile, variation, bpm, key) {
  const full = [
    `Create an original ${profile.label} track at ${bpm} BPM in ${key}, built around ${profile.groove}.`,
    `Use ${profile.guitars}, defined bass, and punchy drums.`,
    `Feature ${profile.colors}; ${profile.vocal}. Keep riffs, hooks, and lead phrases newly composed, with no named performer or existing song references.`,
    variation.structure,
    variation.detail,
    `Aim for ${variation.energy} and no borrowed melody. Mix with separated kick, bass, guitars, leads, vocals, and ambience.`,
  ].join(" ");
  if (full.length <= 1000) return full;

  const compact = [
    `Create an original ${profile.label} track at ${bpm} BPM in ${key}, built around ${profile.groove}.`,
    `Use ${profile.guitars}, defined bass, and punchy drums.`,
    `Feature ${profile.colors}; ${profile.vocal}. Keep riffs, hooks, and lead phrases newly composed, with no named performer or existing song references.`,
    variation.structure,
    `Aim for ${variation.energy} and no borrowed melody. Mix with separated kick, bass, guitars, leads, vocals, and ambience.`,
  ].join(" ");
  if (compact.length <= 1000) return compact;

  return [
    `Create an original ${profile.label} track at ${bpm} BPM in ${key}, built around ${profile.groove}.`,
    `Use ${profile.guitars}, defined bass, punchy drums, and ${profile.colors}.`,
    `${profile.vocal}. Keep riffs, hooks, and lead phrases newly composed, with no named performer or existing song references.`,
    variation.structure,
    `Aim for ${variation.energy} and no borrowed melody. Mix with separated kick, bass, guitars, leads, vocals, and ambience.`,
  ].join(" ");
}

function makeExclude(profile) {
  return unique([
    "specific artist references",
    "copyrighted melodies",
    "copied riffs",
    "existing song titles",
    "famous band signatures",
    "recognizable samples",
    "muddy low end",
    "harsh clipping",
    "thin guitars",
    "weak snare",
    profile.slug.includes("symphonic") || profile.slug.includes("cinematic") ? "copied soundtrack themes" : "",
  ]).join(", ");
}

const records = [];
profiles.forEach((profile, profileIndex) => {
  variations.forEach((variation, variationIndex) => {
    const index = records.length;
    const batchNumber = Math.floor(index / 50) + 1;
    const bpm = profile.bpms[variationIndex];
    const key = profile.keys[variationIndex];
    const title = titleFor(profileIndex, variationIndex);
    const prompt = makePrompt(profile, variation, bpm, key);
    records.push({
      id: `world-metal-heavy-v04-${pad(index + 1)}`,
      source_type: sourceType,
      batch_id: `20260622-world-metal-heavy-${pad(batchNumber)}`,
      collection,
      language_region: profile.region,
      category,
      subcategory: profile.slug,
      title,
      prompt,
      exclude: makeExclude(profile),
      bpm,
      key,
      vocal: profile.vocal,
      mood: profile.mood.split(/,\s*/),
      tags: unique([...profile.tags, profile.region.toLowerCase().replace(/[^a-z0-9]+/g, "-").replace(/^-+|-+$/g, "")]),
      groove_score: 8 + (variationIndex === 0 ? 1 : 0),
      energy: variationIndex === 0 || variationIndex === 3 ? "high" : "medium-high",
      energy_score: variationIndex === 0 || variationIndex === 3 ? 9 : 8,
      public_safe: true,
      rights_status: "ai_generated_original",
      needs_review: false,
      is_top_pick: variationIndex === 0 && profileIndex % 4 === 0,
    });
  });
});

if (records.length !== 200) {
  throw new Error(`Expected 200 records, got ${records.length}`);
}

const titles = new Set();
const ids = new Set();
records.forEach((record) => {
  const titleKey = record.title.toLowerCase();
  if (titles.has(titleKey)) throw new Error(`Duplicate title: ${record.title}`);
  titles.add(titleKey);
  if (ids.has(record.id)) throw new Error(`Duplicate id: ${record.id}`);
  ids.add(record.id);
  if (record.prompt.length < 650 || record.prompt.length > 1000) {
    throw new Error(`${record.id} prompt length out of range: ${record.prompt.length}`);
  }
  if (/\r|\n/.test(record.prompt)) {
    throw new Error(`${record.id} prompt contains newline`);
  }
});

